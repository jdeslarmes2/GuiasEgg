package Entidades;

/**
 *
 * @author jdeslarmes
 */
public class Fecha {
    
    private int dia = 1;
    private int mes = 1;
    private int ano = 1900;
    
    
    
    public Fecha() {
    }

    public Fecha(int dia, int mes, int ano) {
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }
    
    
    
    
    
    
    
}
